


package week1.day1;

public class LearnIf {

	public static void main(String[] args) {
		//condition = true or false
		int number = 0;
		
if(number>0) {
	System.out.println("number is positive");
	
}
else if (number<0) {
	System.out.println("number is negative");
	
}
else {
	//number = 0
	System.out.println("number is zero");
}
	}

}
